#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t count)
{
	unsigned char	*str;
	unsigned char	ch;
	size_t			i;

	str = (unsigned char *)s;
	ch = (unsigned char)c;
	i = 0;
	while (i < count)
	{
		if (*str == ch)
			return (str);
		str++;
		i++;
	}
	return (NULL);
}
